//declare a variable called dogAge (no spaces allowed in variable names)
//holds the actual age of a dog in years
let dogAge;

//holds the calculated age of the dog if it was human
let humanAge;

//store the number 7 inside the variable called dogAge
dogAge = 10;

//store the calculated value of the dog's human age
humanAge = ((dogAge - 1) * 7) + 1;

//print the dog age and calculated human age
console.log('The dog age ' + dogAge + ' is equal to the human age ' + humanAge);

//print just the variables by themselves
console.log(dogAge);
console.log(humanAge);